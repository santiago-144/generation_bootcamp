package santiago_prueba;

public class TipoDeDatos {
	public static void main(String[] args) {
	//8 tipos de datos primitivos 
	int numero=54;
	byte numeroMasPeque=100;
	short numeroPeque=32767;
	long numeroGrande=10000;
	
	float numeroFlotante=1.2f;
	double numeroFlotanteGrande=1.444;
	boolean verdadero=true;
	boolean falso=false;
	char letra='c';
	//dato estrycturado
	String nombre="carlos";
	
	System.out.println(numero);
	System.out.println(numeroMasPeque);
	System.out.println(numeroPeque);
	System.out.println(numeroGrande);
	System.out.println(numeroFlotante);
	System.out.println(numeroFlotanteGrande);
	System.out.println(verdadero);
	System.out.println(falso);
	System.out.println(letra);
	System.out.println(nombre);
	}
	

}
